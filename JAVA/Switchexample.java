package com.ust;

public class Switchexample {
public static void main(String args[]) {
	String str="green";
	switch(str) {
	case "red":
		System.out.println("Iam in red") ;
		System.out.println("**");
		break;
	case "green":
		System.out.println("Iam in green") ;
		System.out.println("**");
		break;
	case "yellow":
		System.out.println("Iam in yellow") ;
		System.out.println("**");
		break;
	default:
		System.out.println("Iam in default") ;
		
		
		
		
		
	}
}
}
